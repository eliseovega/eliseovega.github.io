//app que imprima los números en orden descendente del 10 al 1

var x //Defino mi variable contador

x=Number(prompt("Digite el valor inicial de x: ")) //Capturo el valor de entrada
while (x >= 1) {     //Mientras se cumpla la condicion x <= 10 
	      
	document.write("Los números descendentes:"+x+"<br>") //imprime los número del 10 al 1
	x--                   // contador incrementa x-1 es igual que decir x--
}




